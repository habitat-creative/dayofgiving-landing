// Translation helper
window.t = function(key, replacements) {
  var entry = DOG_I18N[key];
  if (!entry) return key;
  var str = entry[DOG_STATE.lang] || entry.en || key;
  if (replacements) {
    Object.keys(replacements).forEach(function(k) {
      var token = '{' + k + '}';
      while (str.indexOf(token) !== -1) {
        str = str.replace(token, replacements[k]);
      }
    });
  }
  return str;
};

// Currency formatter
window.formatCurrency = function(amount) {
  var num = Math.round(amount);
  if (DOG_STATE.lang === 'fr') {
    return num.toLocaleString('fr-CA') + '\u00a0$';
  }
  return '$' + num.toLocaleString('en-CA');
};

// Percentage formatter — Quebec French: "42 %" (non-breaking space before %)
window.formatPct = function(value) {
  if (DOG_STATE.lang === 'fr') {
    return value + '\u00a0%';
  }
  return value + '%';
};

